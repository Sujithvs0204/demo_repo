rain = 50  # Example value in mm
if rain < 20:
    print("Low risk of crop damage due to drought.")
elif 20 <= rain < 50:
    print("Moderate risk of crop damage due to drought.")
else:
    print("High risk of crop damage due to drought.")

wind = 30  # Example value in km/h
if wind < 20:
    print("Low risk of crop damage due to wind.")
